#include "Calculadora.h"

float Calculadora::somar(float opA, float opB)
{
    return opA + opB;
}

float Calculadora::subtrair(float opA, float opB)
{
    return opA-opB;
}

float Calculadora::multi(float opA, float opB)
{
    return opA*opB;
}

float Calculadora::divisao(float opA, float opB)
{
    return opA/opB;
}
